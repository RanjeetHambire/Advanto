package operator;

public class relationalOperator {

	public static void main(String[] args) {
		int a= 70 , b= 20;
		
		System.out.println("Operator : >  :" + (a > b));
		System.out.println("Operator : <  :" + (a < b));
		System.out.println("Operator : >=  :" + (a >= b));
		System.out.println("Operator : <=  :" + (a <= b));
		System.out.println("Operator : ==  :" + (a == b));
		System.out.println("Operator : !=  :" + (a != b));

	}

}
